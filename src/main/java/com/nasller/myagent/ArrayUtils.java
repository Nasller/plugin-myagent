package com.nasller.myagent;

public class ArrayUtils {
    public static boolean equals(byte[] a, byte[] a2) {
        if (a == null || a2 == null) {
            return false;
        }

        int length = a.length;
        if (a2.length != length) {
            return false;
        }

        for (int i = 0; i < length; i++) {
            if (a[i] != a2[i]) {
                return false;
            }
        }

        return true;
    }
}